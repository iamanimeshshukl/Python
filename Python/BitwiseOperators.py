#Bitwise Operators

#AND 
x,y=12,6
print("x&y=",x&y)

#OR
x,y=11,2
print("x|y=",x|y)

 #NOT 
x=100
print("~x=",~x)

#XOR
x,y=11,3
print("x^y=",x^y)

#RIGHT SHIFT
x,y=100,-100
print("x>>1=", x>>3)
print("y>>1=",y>>3)

 #LEFT SHIFT
x,y=100,-100
print("x<<1=", x<<3)
print("y<<1=",y<<3)