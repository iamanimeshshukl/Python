#Operators in Python  !!

num1 = 4
num2 = 5

#Arithmetic Operators
print("num1 + num2 is ", num1+num2)
print("num1 - num2 is ", num1-num2)
print("num1 * num2 is ", num1*num2)
print("num1 // num2 is ", num1//num2)
print("num1 ** num2 is ", num1**num2)
print("num1 % num2 is ", num1%num2)

#Assignment Operators
a=3
a += 6
print(a)

#Comparison Operators
x = 8
y = 2
z = 8
print(x>y)
print(x<y)
print(x==y)

#Logical Operators
print(x == z and x<y)
print(x == z or x<y)

